package mydiaryweb.module.inferences;


public class DefaultAction {
	public String actionName;
	public String loc;
	public String sv;
	public String face;
	public String mov;
	
public DefaultAction(String actionName, String loc, String sv, String face, String mov) {
		this.actionName = actionName;
		this.loc = loc;
		this.sv = sv;
		this.face = face;
		this.mov = mov;
	}
}
